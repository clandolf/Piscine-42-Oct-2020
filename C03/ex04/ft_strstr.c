char	*ft_strstr(char *str, char *to_find)
{
	int i;

	if (*to_find == 0)
		return (str);
	while (*str)
	{
		if (*str == *to_find)
		{
			i = 1;
			while (to_find[i] && str[i] == to_find[i])
				i++;
			if (to_find[i] == 0)
				return (str);
		}
		str++;
	}
	return (0);
}
